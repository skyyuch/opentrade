-- Align notifications.id with the schema's client-side `@default(uuid())`.
--
-- `notifications` was created (20260527104000) with a server-side
-- `DEFAULT gen_random_uuid()`, but schema.prisma declares
-- `id String @id @default(uuid()) @db.Uuid`, which Prisma generates
-- client-side (no DB default). Every other `@default(uuid())` table in this
-- schema has no DB default; three sibling tables briefly created with
-- `gen_random_uuid()` were already aligned this exact way in 20260523022745.
-- `notifications` was created after that cleanup and was missed, leaving it the
-- lone column drifting from the schema and making `prisma migrate dev`
-- repeatedly want to emit this statement (deferred through the Prisma 7 / Next
-- 16 upgrade PRs; resolved here in a dedicated chore).
--
-- Pure DDL, no data migration (per rule 31). UUIDs are still generated by
-- Prisma at insert time, so existing rows and the notifications domain are
-- unaffected.
ALTER TABLE "notifications" ALTER COLUMN "id" DROP DEFAULT;
